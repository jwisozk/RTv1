#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;

	str = (char*)s;
	while (*str != c && *str != '\0')
		str++;
	if (*str == '\0' && c != '\0')
		return (NULL);
	return (str);
}
